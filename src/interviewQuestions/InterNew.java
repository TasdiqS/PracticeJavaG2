package interviewQuestions;

public class InterNew {

	public static void main(String[] args) {
		Integer A = 100;
		Integer B = 100;
		Integer C = 200;
		Integer D = 200;

		if (A == B) {
			System.out.println("A equal to B");
		} else {
			System.out.println("A not equal to B");
		}

		if (C == D) {
			System.out.println("C equal to D");
		} else {
			System.out.println("C not equal to D");
		}

	}

}
